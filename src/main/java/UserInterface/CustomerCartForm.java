    private void RemoveOrderButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RemoveOrderButtonMouseClicked
        // TODO add your handling code here:
        String itemName = ItemNameTextField.getText();
        int itemQuantity = Integer.valueOf(ItemQuantitySpinner.getValue().toString());
        int customerID = 2;
        try{
            if(Client.Object.removeOrder(customerID, itemName, itemQuantity)){
                JOptionPane.showMessageDialog(null, itemName +" deleted from Order!");
                refresh();
            }else{
                JOptionPane.showMessageDialog(null, "Ordered item quantity not enough, please try again!");
            }
        }catch(Exception ex){
            Logger.getLogger(CustomerPlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_RemoveOrderButtonMouseClicked

    private void editOrderButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editOrderButtonMouseClicked
        // TODO add your handling code here:
        String itemName = ItemNameTextField.getText();
        int itemQuantity = Integer.valueOf(ItemQuantitySpinner.getValue().toString());
        int customerID = 2;
        try {
            if(Client.Object.updateCart(customerID, itemName, itemQuantity)){
                JOptionPane.showMessageDialog(null, "Item"+itemName+" updated Successfully!");
                refresh();
            }else{
               JOptionPane.showMessageDialog(null, "Item Quantity not available in inventory, item can't be updated!");
               
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerCartForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editOrderButtonMouseClicked


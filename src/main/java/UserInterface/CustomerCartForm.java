    private void cartTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cartTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)cartTable.getModel();
        int selectedItemRowIndex = cartTable.getSelectedRow();
        
        ItemNameTextField.setText(model.getValueAt(selectedItemRowIndex, 0).toString());
        ItemPriceTextField.setText(model.getValueAt(selectedItemRowIndex, 1).toString());
        ItemTotalPriceTextField.setText(model.getValueAt(selectedItemRowIndex, 3).toString());
        ItemQuantitySpinner.setValue(model.getValueAt(selectedItemRowIndex, 2));
    }//GEN-LAST:event_cartTableMouseClicked

    private void ItemQuantitySpinnerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_ItemQuantitySpinnerStateChanged
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)cartTable.getModel();
        double itemPricePerUnit = Double.parseDouble(ItemPriceTextField.getText());
        Integer itemQuantity = (Integer)ItemQuantitySpinner.getValue();
        
        if(itemQuantity<0){
            JOptionPane.showMessageDialog(null, "Invalid quantity! Please try again");
            ItemQuantitySpinner.setValue(0);
        }
        else{
            double itemTotalPrice = itemPricePerUnit*itemQuantity;
            ItemTotalPriceTextField.setText(String.valueOf(itemTotalPrice));
        }
    }//GEN-LAST:event_ItemQuantitySpinnerStateChanged

    private void searchOrderItemLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchOrderItemLabelMouseClicked
        // TODO add your handling code here:
        String searchItem = searchItemTextField.getText();
        try{
            if(Client.Object.searchCart(searchItem)){
                String searchedItem = Client.Object.getCartItem(searchItem);
                DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
                model.setRowCount(0);
                String[] splitSearchedItem = searchedItem.split(",");
                model.addRow(splitSearchedItem);
            }
            else{
                searchItemTextField.setText("Search Item");
                JOptionPane.showMessageDialog(null, searchItem+" not Found in cart! Please try again");
                
            }
        }catch(Exception ex) {
            Logger.getLogger(CustomerPlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchOrderItemLabelMouseClicked

